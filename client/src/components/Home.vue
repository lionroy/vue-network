    grid-list-md
    text-xs-center
    fluid
  >
    <v-layout
      row
      wrap
    >

      <v-flex md12>
    <font-awesome-icon icon="coffee" />  
        <v-parallax
          height="400"
          dark
          src="https://cdn.vuetifyjs.com/images/backgrounds/vbanner.jpg"
        >
          <v-layout
            align-center
            column
            justify-center
          >
                <h1 class="display-4 amber--text text--lighten-2">PRONTO!</h1>
                <p>Projects Go Social</p>

          </v-layout>
        </v-parallax>
      </v-flex>
    </v-layout>

    <v-layout
      class="mt-2 mb-3"
      row
      wrap
      v-if="!loading"
    >

    </v-layout>
    <v-layout row>
      <v-dialog
        v-model="loading"
        persistent
        fullscreen
      >
        <v-container fill-height>
          <v-layout
            row
            justify-center
            align-center
          >
            <v-progress-circular
              indeterminate
              :size="70"
              :width="7"
              color="secondary"
            ></v-progress-circular>
          </v-layout>
        </v-container>
      </v-dialog>
    </v-layout>

    <!-- Carousel Posts -->
    <v-flex xs12>
      <v-card position="relative">
        <v-carousel
          v-if="!loading && posts.length > 0"
          v-bind="{ 'cycle': true }"
          interval="10000"
        >
          <v-carousel-item
            v-for="post in posts"
            :key="post._id"
            :src="post.imageUrl"
            @click.native="goToPost(post._id)"
          >
            <h1 id="carousel__title">{{post.title}}</h1>
          </v-carousel-item>
        </v-carousel>

      </v-card>

    </v-flex>
  </v-container>
</template>

<script>
import { mapGetters } from "vuex";

export default {
  name: "home",
  created() {
    this.handleGetCarouselPosts();
  },
  computed: {
    ...mapGetters(["loading", "posts"])
  },
  methods: {
    handleGetCarouselPosts() {
      // reach out to Vuex store, fire action that gets posts for carousel
      this.$store.dispatch("getPosts");
    },
    goToPost(postId) {
      this.$router.push(`/posts/${postId}`);
    }
  }
};
</script>

